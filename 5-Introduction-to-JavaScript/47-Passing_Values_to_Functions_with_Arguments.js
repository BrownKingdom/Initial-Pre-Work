<!-- Using the parameters feature set placeholders for values to be entered into a function. Values entered "passed" are called arguments. -->
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

